
public class palestrante {

	String nomeDopalestrante;
	String fone;
	double valorHora;
	
	
	public palestrante (String nomeDopalestrante, String fone, double valorHora) {
		
		super();
		this.nomeDopalestrante = nomeDopalestrante;
		this.fone = fone;
		this.valorHora = valorHora;
		
		
		
		
		
		
	}


	public String getNomeDopalestrante() {
		return nomeDopalestrante;
	}


	public void setNomeDopalestrante(String nomeDopalestrante) {
		this.nomeDopalestrante = nomeDopalestrante;
	}


	public String getFone() {
		return fone;
	}


	public void setFone(String fone) {
		this.fone = fone;
	}


	public double getValorHora() {
		return valorHora;
	}


	public void setValorHora(double valorHora) {
		this.valorHora = valorHora;
	}
		
		
	
	
		
	
	
	
	
   
	
	
	
	
}
